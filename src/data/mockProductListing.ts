/**
 * Get products by category
 */
export function getProductsByCategory(category: string): ProductListingCard[] {
  return getMockProductListingCards().filter(product => product.imageKind === category);
}

/**
 * Get products with promo badges
 */
export function getPromoProducts(): ProductListingCard[] {
  return getMockProductListingCards().filter(product => product.promo !== undefined);
}

/**
 * Get verified supplier products
 */
export function getVerifiedProducts(): ProductListingCard[] {
  return getMockProductListingCards().filter(product => product.verified === true);
}

/**
 * Get products sorted by rating
 */
export function getTopRatedProducts(): ProductListingCard[] {
  return [...getMockProductListingCards()].sort((a, b) => {
    const ratingA = a.rating ?? 0;
    const ratingB = b.rating ?? 0;
    return ratingB - ratingA;
  });
}

/**
 * Get products sorted by sales count
 */
export function getBestSellingProducts(): ProductListingCard[] {
  return [...getMockProductListingCards()].sort((a, b) => {
    const salesA = parseInt(a.stats.replace(/[^0-9]/g, '')) || 0;
    const salesB = parseInt(b.stats.replace(/[^0-9]/g, '')) || 0;
    return salesB - salesA;
  });
}

/**
 * Search products by name (case-insensitive)
 */
export function searchProducts(query: string): ProductListingCard[] {
  const lowerQuery = query.toLowerCase();
  return getMockProductListingCards().filter(product =>
    product.name.toLowerCase().includes(lowerQuery)
  );
}
